package com.bizwave.bamstory;

public class EvalInfo {
	private int evalCount;
	private String facilityImgName;
	private String priceImgName;
	private String contactImgName;
	private String girlImgName;
	private String reVisitImgName;
	public int getEvalCount() {
		return evalCount;
	}
	public void setEvalCount(int evalCount) {
		this.evalCount = evalCount;
	}
	public String getFacilityImgName() {
		return facilityImgName;
	}
	public void setFacilityImgName(String facilityImgName) {
		this.facilityImgName = facilityImgName;
	}
	public String getPriceImgName() {
		return priceImgName;
	}
	public void setPriceImgName(String priceImgName) {
		this.priceImgName = priceImgName;
	}
	public String getContactImgName() {
		return contactImgName;
	}
	public void setContactImgName(String contactImgName) {
		this.contactImgName = contactImgName;
	}
	public String getGirlImgName() {
		return girlImgName;
	}
	public void setGirlImgName(String girlImgName) {
		this.girlImgName = girlImgName;
	}
	public String getReVisitImgName() {
		return reVisitImgName;
	}
	public void setReVisitImgName(String reVisitImgName) {
		this.reVisitImgName = reVisitImgName;
	}
	
	
}
